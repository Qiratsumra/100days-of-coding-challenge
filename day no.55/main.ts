// Data types:
// string:
let message:string  =  "Hello World";

// number:
let number :number = 3;

// bigint:
let be = 323232232;
console.log(be); //output will be 323232232;
console.log(9007199254740991 + 2);  // output wil be 9007199254740992;

// boolean:
let num1=45;
let num2 = 50;
console.log(num1 === num2); //output is false;

// null:
// It’s just a special value which represents “nothing”, “empty” or “value unknown”.
let nullValue:null=null;
console.log(nullValue);

// undefined:
// The meaning of undefined is “value is not assigned”.
let personName ;
console.log(personName);

// typeof:
console.log(typeof personName); // output will be undefined;
console.log(typeof message); //output will be string;
console.log(typeof num1); //output will be number;
console.log(typeof nullValue); //output will be null;
console.log(typeof Symbol("id")); //output will be symbol

// alert:
console.log(alert("Hello"));






